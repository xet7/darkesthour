#!/bin/bash

#====== SETTINGS START =======

# Name with minus. Could also be without minus.
# This is:
# - filename of C sourcecode like darkest-hour.c
# - filename of executeable like darkest-hour on some operating systems
NAMEMINUS=darkest-hour
# Name for DOS that has 8 characters, will result to FILENAME.EXE:
NAMEDOS=DARKHOUR
# Name CamelCase on some retro platforms:
NAMECAMELCASE=DarkestHour
# Name with space, name of the software in top of menu:
NAMESPACE="Darkest Hour"

#====== SETTINGS END =======
